    public void cloneData() {
        list.clear();
        String temp = txtFind.getText().toLowerCase();
        for (NhanVien sv : sv.getAll()) {
            String ten = sv.getHoten().toLowerCase();
            if (ten.contains(temp)) {
                list.add(sv);
            }
        }
        loadDataTable();
    }

    public void loadDataTable() {
        model = new DefaultTableModel();
        //Tạo các cột
        model.addColumn("MSNV");
        model.addColumn("HO TEN");
        model.addColumn("QUE QUAN");
        model.addColumn("CCCD");
        model.addColumn("GIOI TINH");
        model.addColumn("SDT");
        model.addColumn("LUONG");
        //model.addColumn("HINH ANH");
        //model.addColumn("TRANG THAI");
        fillToTable();
    }

    public void fillToTable() {
        //cloneData();
        //System.out.println(listLaptop.get(0).getMaLoai());
        model.setRowCount(0);
        String gioiTinh = "Nu";
        for (NhanVien index : list) {
            if (index.isGioitinh()) {
                gioiTinh = "Nam";
            }
            model.addRow(new Object[]{index.getMsnv(), index.getHoten(), index.getQuequan(), index.getCccd(), gioiTinh, index.getSdt(), index.getLuong()});
        }
        tblList.setModel(model);
    }

