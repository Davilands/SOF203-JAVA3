    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int i = tblList.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(this, "Vui long chon doi tuong cap nhat!");
            return;
        }
        boolean gt = false;
        float luong = 0;
        try {
            luong = Float.valueOf(txtLuong.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Luong khong hop le");
            return;
        }
        int ketQua = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn cập nhật?", "Cập nhật Nhân viên", JOptionPane.YES_NO_OPTION);
        if (ketQua == JOptionPane.YES_OPTION) {
            if (rdoNam.isSelected()) {
                gt = true;
            }

            for (NhanVien sv : list) {
                if (sv.getMsnv().equalsIgnoreCase(txtMSSV.getText())) {
                    sv.update(txtMSSV.getText(), txtHoTen.getText(), txtQueQuan.getText(), txtCCCD.getText(), gt, this.hinhAnh, txtSdt.getText(), luong);
                    JOptionPane.showMessageDialog(this, "Cap nhat thanh cong!");
                    cloneData();
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "Khong the cap nhat");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    public void saveFile(int index) throws FileNotFoundException, IOException {
        String gt = "Nam";
        if (!list.get(index).isGioitinh()) {
            gt = "Nữ";
        }
        String[] data = {
            /*
                String.valueOf(txtUserName.getText()),
                String.valueOf(txtPassword.getText()),
                String.valueOf(txtConfirmPassword.getText())
             */
            "\t Thông tin nhân viên: " + list.get(index).getHoten() + "\n",
            "Mã số nhân viên: " + list.get(index).getMsnv() + "\n",
            "Tên nhân viên: " + list.get(index).getHoten() + "\n",
            "số CCCD: " + list.get(index).getCccd() + "\n",
            "Quê quán: " + list.get(index).getQuequan() + "\n",
            "Số điện thoại: " + list.get(index).getSdt() + "\n",
            "Giới tính: " + gt + "\n",
            "Lương: " + list.get(index).getLuong() + "\n",
            "\n\n\n"
        };

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Chọn đường dẫn lưu văn bản!");

        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            //WriteToFile(fileToSave.getAbsolutePath() + ".docx");

            /*  
                abcde.docx
                x : length-1
                . : length-5
             */
            String path = fileToSave.getAbsoluteFile().toString();
            String file = fileToSave.getAbsolutePath();
            if (!path.contains(".doc")) {
                file = fileToSave.getAbsolutePath() + ".doc";
            }

            OutputStream outputStream = new FileOutputStream(file);
            OutputStreamWriter outputStreamWriter = new OutputStreamWriter(outputStream);

            for (String item : data) {
                outputStreamWriter.write(item);
                // Dùng để xuống hàng
                outputStreamWriter.write("\n");
            }
            // Đây là phương thức quan trọng!
            // Nó sẽ bắt chương trình chờ ghi dữ liệu xong thì mới kết thúc chương trình.
            outputStreamWriter.flush();
            JOptionPane.showMessageDialog(this, "Đã in thông tin nhân viên vào: " + path);
        }
    }

